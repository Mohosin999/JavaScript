// Arithmetic Operator
// +, -, *, /, %, ++, --

let a = 10;

// Increment Operator ++
// Pre Increment -> Post Increment

//Pre Increment
console.log(++a); // 11

// Post Increment
console.log(a++); // 10
console.log(a); // 11

// same logic for Decrement

